package session3;

public class Operators {
    public static void main(String[] args) {
        int x = 13;
        int y = 5;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println(x%y);
        System.out.println(x++);
        System.out.println(y--);
    }
}
